# In this lecture we will have a look at different kinds of data types in Python
# Unlike other programming languages python dynamically assigns the data type to its variables

x = 10              # Here python will make x an integer variable automatically
y = 10.5            # Here python will make y a float variable automatically
z = "Any Word"      # Here python will make z a string variable automatically

# ---------------- New we can use type function of python to check the data type of variable
print(type(x))
print(type(y))
print(type(z))
