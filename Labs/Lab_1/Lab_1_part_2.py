# Creating a dictionary datatype in python

#               key   :   ans,   key : ans,   key   : ans
studentDict_1 = {"name": "John", "age": 36, "marks": 40}
print(studentDict_1)                     # printing a dictionary
# Getting values from the dictionary using keys
print("Name of student:")
print(studentDict_1["name"])             # Getting value against the key -- name
print("Age of student:")
print(studentDict_1["age"])              # Getting value against the key -- age
print("Marks of student:")
print(studentDict_1["marks"])            # Getting value against the key -- marks




